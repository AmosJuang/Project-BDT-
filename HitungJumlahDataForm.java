    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String selectedCountry = (String) jComboBox1.getSelectedItem();
       String selectedYear = (String) jComboBox3.getSelectedItem();
       String selectedOilType = (String) jComboBox4.getSelectedItem();
       String selectedDistributionType = (String) jComboBox5.getSelectedItem();
       try{
           Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
           Connection con = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=PROJECTBDT;encrypt=true;trustServerCertificate=true","sa","123");
                String query = "SELECT COUNT(*) AS TotalCount FROM Fact "
                     + "INNER JOIN Country ON Fact.country_id = Country.country_id "
                     + "INNER JOIN YearDim ON Fact.year_id = YearDim.year_id "
                     + "INNER JOIN Product ON Fact.product_id = Product.product_id "
                     + "INNER JOIN Flow ON Fact.flow_id = Flow.flow_id "
                     + "WHERE 1=1";
        
       
        if (!"ALL".equals(selectedCountry)) {
            query += " AND Country.country_name = ?";
        }
        if (!"ALL".equals(selectedYear)) {
            query += " AND YearDim.year = ?";
        }
        if (!"ALL".equals(selectedOilType)) {
            query += " AND Product.product_name = ?";
        }
        if (!"ALL".equals(selectedDistributionType)) {
            query += " AND Flow.flow_name = ?";
        }
        
      
        PreparedStatement pst = con.prepareStatement(query);
        
        
        int parameterIndex = 1;
        if (!"ALL".equals(selectedCountry)) {
            pst.setString(parameterIndex++, selectedCountry);
        }
        if (!"ALL".equals(selectedYear)) {
            pst.setInt(parameterIndex++, Integer.parseInt(selectedYear));
        }
        if (!"ALL".equals(selectedOilType)) {
            pst.setString(parameterIndex++, selectedOilType);
        }
        if (!"ALL".equals(selectedDistributionType)) {
            pst.setString(parameterIndex++, selectedDistributionType);
        }
        ResultSet rs = pst.executeQuery();
        if (rs.next()) {
            int count = rs.getInt("TotalCount");
            DefaultTableModel model = (DefaultTableModel) Tbl_data.getModel();
            model.setRowCount(0);
            model.addRow(new Object[]{count});
        }
        
       
        rs.close();
        pst.close();
        con.close();
            }catch (Exception e){
           JOptionPane.showMessageDialog(null, e);
       }
    }//GEN-LAST:event_jButton1ActionPerformed

