    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String selectedCountry = (String) jComboBox1.getSelectedItem();
        String selectedYear = (String) jComboBox2.getSelectedItem();
        String selectedOilType = (String) jComboBox3.getSelectedItem();
        String selectedDistributionType = (String) jComboBox4.getSelectedItem();
       try{
           Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
           Connection con = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=PROJECTBDT;encrypt=true;trustServerCertificate=true","sa","123");
           String query = "SELECT C.country_name AS country, P.product_name AS product, F.flow_name AS distribution_type, " +
               "       Y.year AS year, Fact.type, Fact.value " +
               "FROM Fact " +
               "JOIN Country C ON Fact.country_id = C.country_id " +
               "JOIN Product P ON Fact.product_id = P.product_id " +
               "JOIN Flow F ON Fact.flow_id = F.flow_id " +
               "JOIN YearDim Y ON Fact.year_id = Y.year_id " +
               "WHERE C.country_name = ? AND Y.year = ? AND P.product_name = ? AND F.flow_name = ?";
           PreparedStatement stmt = con.prepareStatement(query);
           stmt.setString(1, selectedCountry);
           stmt.setString(2, selectedYear);
           stmt.setString(3, selectedOilType);
           stmt.setString(4, selectedDistributionType);
           
           ResultSet rs = stmt.executeQuery();

           
           javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) Tbl_data.getModel();
           model.setRowCount(0);

           
           while (rs.next()) {
               String country = rs.getString("country");
               String product = rs.getString("product");
               String distributionType = rs.getString("distribution_type");
               String year = rs.getString("year");
               String type = rs.getString("type");
               double value = rs.getDouble("value");

               model.addRow(new Object[]{country, type, product, distributionType, year, value});
           }
           JOptionPane.showMessageDialog(null, "Data Berhasil Dicari.");
           rs.close();
           stmt.close();
           con.close();
           

       }catch (Exception e){
           JOptionPane.showMessageDialog(null, e);
       }
           
    }//GEN-LAST:event_jButton2ActionPerformed

